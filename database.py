def add_plant(name, description, care, pests, diseases):
    conn = sqlite3.connect('plants.db')
    cursor = conn.cursor()
    cursor.execute('''
        INSERT OR REPLACE INTO plants (name, description, care, pests, diseases)
        VALUES (?, ?, ?, ?, ?)
    ''', (name, description, care, pests, diseases))
    conn.commit()
    conn.close()

# –§—É–Ω–∫—Ü–∏—è –¥–ª—è –ø–æ–ª—É—á–µ–Ω–∏—è –¥–∞–Ω–Ω—ã—Ö –æ —Ä–∞—Å—Ç–µ–Ω–∏—è—Ö
def get_plant(name):
    conn = sqlite3.connect('plants.db')
    cursor = conn.cursor()
    cursor.execute('SELECT name, description, care, pests, diseases FROM plants WHERE name = ?', (name,))
    plant = cursor.fetchone()
    conn.close()
    return plant

